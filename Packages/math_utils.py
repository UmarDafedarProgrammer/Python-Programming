# math_utils.py - module
# To import use: import math_util
def addition(a,b):
    return a+b

def substraction(a,b):
    return a-b

def multiplication(a,b):
    return a*b

def division(a,b):
    return a/b

def remainder(a,b):
    return a%b

def floor_division(a,b):
    return a//b

def square(a):
    return a**a
