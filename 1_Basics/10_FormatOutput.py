
# Using % Operator
# We can use '%' operator. % values are replaced with zero or more value of elements. 
# The formatting using % is similar to that of ‘printf’ in the C programming language.

# %d –integer
# %f – float
# %s – string
# %x –hexadecimal
# %o – octal

i = 10
f = 3.456
s = "string"
b = True

print("value of i %d" %i)
print("value of f %f" %f)
print("value of .2f %.2f" %f)
print("value of s %s" %s)
print("value of hexadecimal i %x" %i)
print("value of octal i %o" %i)